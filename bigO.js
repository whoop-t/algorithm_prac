/**
 * * BigO
 * How we measure speed/memory used with our code
 * Time Complexity - Time it takes
 * Space Complexity - Memory it uses
 *
 * * Time/Space Complexity
 * O(1) - as n grow, no change, constant
 * O(log n) - next best
 * O(n) - linear growth, grows as n grows
 * O(nlog n) - not that great
 * O(n^2) -  quadratic/exponential growth(nested loop)
 *
 *
 * * Space Complexity
 * Most primitives are constant space
 * String, reference types(arrays, objects) require O(n) - n is length
 */
