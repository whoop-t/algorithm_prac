/**
 * * Problem Solving
 *
 * ========================
 * * Understand the Problem
 * ========================
 * Can you restate the problem in your own words?
 * What are the inputs that go into the problem? What are the outputs?
 * Can outputs be determined from the inputs?
 * How should I label the importnat pieces of data?
 *
 * ===========================
 * * Explore Concrete Examples
 * ===========================
 * Start with a simple examples, input and output
 * Progress to more complex examples
 * Explore Edge Cases
 * Explore Invalid Inputs
 *
 * ===============
 * * Break It Down
 * ===============
 * Write out steps you need to take, forces you to think
 *
 * ================
 * * Solve/Simplify
 * ================
 * Try and solve, or simplify solution if not 100%
 *
 * ========================
 * * Look Back and Refactor
 * ========================
 * Look back at solution and see what could be done better, what can be refactored
 */
